def colorize_ascii_art(ascii_art):
    for line in ascii_art:
        colored_line = ""
        for char in line:
            if char != "*":
                # Wechseln Sie zwischen verschiedenen Grüntönen und anderen Farben
                colored_line += Style.GREEN2(char) if char in "█╗╝" else Style.GREEN(char)
            else:
                colored_line += char
        print(colored_line)
